!***********************************************************************
      module mod_swap
!***********************************************************************
      use iso_fortran_env, only : &
         r32=>real32, r64=>real64, i32=>int32, i64=>int64
      implicit none
      interface swap
         module procedure swap_r64
         module procedure swap_r32
         module procedure swap_i64
         module procedure swap_i32
      end interface
      contains
     !******************************************************************
         subroutine swap_r64(a,b)
     !******************************************************************
         real(r64) :: a,b,t
         t=a; a=b; b=t
         end
     !******************************************************************
         subroutine swap_r32(a,b)
     !******************************************************************
         real(r32) :: a,b,t
         t=a; a=b; b=t
         end
     !******************************************************************
         subroutine swap_i64(a,b)
     !******************************************************************
         integer(i64) :: a,b,t
         t=a; a=b; b=t
         end
     !******************************************************************
         subroutine swap_i32(a,b)
     !******************************************************************
         integer(i32) :: a,b,t
         t=a; a=b; b=t
         end
      end
